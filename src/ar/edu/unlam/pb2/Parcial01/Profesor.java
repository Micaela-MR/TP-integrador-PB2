package ar.edu.unlam.pb2.Parcial01;

public class Profesor {

	private Integer id;
	private Integer dni;
	private String nombre;
	private String apellido;
	
	public Profesor(Integer id, Integer dni, String nombre, String apellido) {
		this.id = id;
		this.dni = dni;
		this.nombre = nombre;
		this.apellido = apellido;
	}
	
}
